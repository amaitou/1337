int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while (i <= (nb / 2))
	{
		if ((nb % i) != 0)
			++i;
		else
			break ;
	}
	i--;
	if (i == (nb / 2))
		return (1);
	return (0);
}
