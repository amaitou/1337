int	ft_str_is_uppercase(char *str)
{
	int		i;
	char	c;

	i = 0;
	c = 1;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			c = 1;
		else
			return (0);
		++i;
	}
	return (c);
}
