int	ft_str_is_numeric(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (*(str + i))
	{
		if (str[i] >= '0' && str[i] <= '9')
			c = 1;
		else
			return (0);
		++i;
	}
	return (c);
}
